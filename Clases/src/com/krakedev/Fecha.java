package com.krakedev;

public class Fecha {
	private int mes ;
	private int anio ;
	private int dia ;
	
	public Fecha () {
		
	}
	
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getAnio() {
		return anio;
	}
	public void setAnio(int anio) {
		this.anio = anio;
	}
	public int getDia() {
		return dia;
	}
	public void setDia(int dia) {
		this.dia = dia;
	}
	
	

	
	 
} 
